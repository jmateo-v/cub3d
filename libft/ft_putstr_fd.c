#include "libft.h"
#include <stdlib.h>

void	ft_putstr_fd(const char *s, int fd)
{
	if (s == NULL)
		return ;
	while (*s != '\0')
	{
		ft_putchar_fd(*s, fd);
		s++;
	}
}
